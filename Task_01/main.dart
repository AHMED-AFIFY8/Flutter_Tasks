void main() {
int degree = 99;
if (degree >= 95) {
    print('Degree: +A');
  } else if (degree >= 90) {
    print('Degree: -A');
  } else if (degree >= 85) {
    print('Degree: +B');
  } else if (degree >= 80) {
    print('Degree: -B');
  } else if (degree >= 75) {
    print('Degree: +C');
  } else if (degree >= 70) {
    print('Degree: -C');
} else if (degree >= 65) {
    print('Degree: +D');
  } else if (degree >= 60) {
    print('Degree: -D');
  } else {
    print('Degree: F');
  }
}
